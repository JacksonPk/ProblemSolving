let N = Int(readLine()!)!
var arr = Array(repeating: Array(repeating: " ", count: N), count: N)
var result = ""
func recursion(n: Int,row: Int,col:Int) {
    let k = n/3
    
    if k == 1 {
        for i in row..<row+3 {
            for j in col..<col+3 where i != (row+1) || j != (col+1) {
                arr[i][j] = "*"
            }
        }
        return
    }
    
    for i in 0..<3 {
        for j in 0..<3 where i != 1 || j != 1 {
            recursion(n: k, row: row+(k*i), col: col+(k*j))
        }
    }
}

recursion(n: N, row: 0, col: 0)

for i in 0 ..< arr.count {
    for j in 0..<arr.count {
        result.write(arr[i][j])
    }
    result.write("\n")
}
print(result)

